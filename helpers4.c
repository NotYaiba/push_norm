#include"push_swap.h"

int check_str(char *str)
{
    int i;

    i = 0;
    if (str[i] == '-')
        i++;
    while (str[i])
    {
        if (isdigit(str[i]) != 1)
            return (0);
        i++;
    }
    return (1);
}